// =============================================================================
// 💰 資金管理とリスク管理の心構え
// =============================================================================

/**
 * 資金管理の優先順位
 *
 * 「何回も言うけど勝率上げるより、資金管理とリスクリワードの方が大事」
 *
 * - 勝率50%でも損益率3あれば破産しない
 * - 高勝率システムはギャンブルそのもの、破産を早める可能性
 * - 勝率は50%が一番バランスが良い
 * - リスクリワード1:2では連続負けでメンタル崩壊しやすい
 */

/**
 * 修正で切り取る重要性
 *
 * なぜ修正箇所で切り取るのか：
 *
 * 1. 相場の特性上、一方向に動き続けないから
 *    - 価格が急上昇すると利益確定の売り注文が増える
 *    - 新規の売り注文が発生する
 *    - 上昇が一時的に止まる
 *
 * 2. 全部取ろうとすると「全戻し」を食らいやすい
 *    - 一度大きく利益が出たのに、その後価格が逆行
 *    - 利益がゼロ（もしくはマイナス）になる
 *
 * 3. リスクを抑えられる
 *    - 修正のタイミングで利益を確保
 *    - 大きな動きに翻弄されない
 *
 * 4. 精神的な負担を軽減できる
 *    - 「もっと利益を取れるかも！」という欲張りを避ける
 *    - 小さく満足することの重要性
 *
 * 5. トレードの継続性を保つ
 *    - 確実に利益を積み上げる
 *    - 次のトレードに向けた資金を確保
 */

// =============================================================================
// 📊 収支状況別の心構え
// =============================================================================

/**
 * プラスが続いている時の心構え
 *
 * - 勝ちトレードの流れに乗る
 * - 少しずつロットを増やす（10%ずつ）
 * - 勝ち金の一部を次のトレードに使わない資金として分離
 * - 大きく伸びたら建値にSLを置く
 * - 勝ち癖による油断をしない
 * - 「自分は負けない」という慢心から過剰なリスクを取らない
 */

/**
 * 期待値が高い場面での心構え
 *
 * - リスクを限定したエントリー（資金の1-2%）
 * - ポジション分割で安全性を高める
 * - ピラミッディングの活用
 * - 損切りラインをエントリー価格より上に設定してノーリスク状態に
 * - 期待値に過度に依存しない
 * - 相場の転換に敏感になる
 */

/**
 * マイナスが続いている時の心構え
 *
 * - メンタルを保ち、資金を守る
 * - ロットを50%以下に減らす
 * - エントリー回数を減らす
 * - 明確なトレードチャンスが来るまで待つ
 * - 資金の5%を失ったらその日はトレード中止
 * - 短期決戦を意識、小さな利益で十分と割り切る
 * - 損切りを厳守、より厳しくする
 * - 取り返そうと焦らない
 * - 結果ではなく過程にフォーカス
 */

