/**
 * テクニカル分析の3つの基本前提
 *
 * 1. 市場の動きは全てを織り込んでいる
 *    - 現在の価格には、あらゆる情報、ニュース、経済指標、
 *      投資家の心理や期待、不安が全て反映されている
 *    - チャートは市場の縮図として目の前にある
 *    - 難しい経済学の知識や膨大なニュースを追いかけなくても、
 *      チャート分析で市場参加者の感情が読み取れる
 *
 * 2. 価格はトレンドを形成する
 *    - 相場は必ず上下に動く特性がある
 *    - 一方的に上がり続けることや下がり続けることはない
 *    - 必ずどこかで「修正」＝一時的な反転や調整が起こる
 *
 * 3. 歴史は繰り返す
 *    - 相場は「人間の思考」に基づくもの
 *    - パターンと確率の世界
 */

